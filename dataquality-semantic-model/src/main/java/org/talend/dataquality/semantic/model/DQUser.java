package org.talend.dataquality.semantic.model;

import java.io.Serializable;

public class DQUser implements Serializable {

    private String id;

    private String firstName;

    private String username;

    private String lastName;

    private String email;

    public DQUser(String id) {
        this.username = id;
    }

    public DQUser(String id, String firstName, String lastName, String email) {
        this.id = id;
        this.firstName = firstName;
        this.lastName = lastName;
        this.email = email;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getFirstName() {
        return firstName;
    }

    public void setFirstName(String firstName) {
        this.firstName = firstName;
    }

    public String getLastName() {
        return lastName;
    }

    public void setLastName(String lastName) {
        this.lastName = lastName;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    @Override
    public String toString() {
        return String.format("User [ID=%s username=%s firstName=%s  lastName=%-20s  email=%-20s]", id, username, firstName,
                lastName, email);
    }

}
